    private void CancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CancelButtonActionPerformed
        
        CancelDisplay.setText("");
        String Cname=cancelName.getSelectedItem().toString();
        Date Fdate=Date.valueOf(cancelDate.getSelectedItem().toString());
        Book book=BookingList.deleteBook(Cname,Fdate);
        if (book==null) {
            CancelDisplay.append(String.format("Invalid input.%n Customer: %s does not have any flight on %s\n", 
                    Cname, Fdate.toString()));
        }
        else{
            CancelDisplay.append(String.format("Successfully delete booking for customer: %s\n"
                    + " for Flight: %s\n"
                    + " on %s\n\n\n", book.getName(),book.getFlight(),book.getDate().toString()));
        Book newbook=WaitList.updatetoBook(book);
        if (newbook!=null) {
            BookingList.newBook(newbook.getName(), newbook.getDate(), newbook.getFlight());
            CancelDisplay.append(String.format("Customer (%s) is automatically booked from Wait List with: \n"
                    + "Date: %s\n" + "Flight: %s\n"+"TimeStamp: %s\n\n\n",
                    newbook.getName(),
                    newbook.getDate().toString(),
                    newbook.getFlight(),
                    newbook.getTimeStamp().toString()));
        }
        }
        AddNameComboBox();
    }//GEN-LAST:event_CancelButtonActionPerformed

    private void DropButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropButtonActionPerformed
        DropDisplay.setText("");
        String dropflight=DropFlightList.getSelectedItem().toString();
        int j=FlightList.dropFlight(dropflight);
        DropDisplay.append(String.format("drop Flight: %s successfully\n\n", dropflight));     
        List<Book> booklist=BookingList.findbyFlight(dropflight);
        int Nrow=BookingList.dropbyFlight(dropflight);
        List<Flight> FList=FlightList.getFlightList();
        List<Book> newBookList=new ArrayList<>();;
        boolean booked=false;
        List<String> faillist=new ArrayList<>();;
        if (Nrow>0) {
            for (Book book: booklist) {
                for (Flight F:FList) {
                    if (BookingList.getEmptySeats(book.getDate(), F.getFlightNum())>0) {
                        BookingList.newBook(book.getName(),book.getDate(), F.getFlightNum());
                        booked=true;
                        Timestamp currentTimeStamp=new Timestamp(Calendar.getInstance().getTime().getTime());
                        DropDisplay.append(String.format("Customer: %s is rebooked with:\n"
                                + "Date: %s\n" + "Flight: %s\n"+"TimeStamp: %s\n\n",book.getName(),
                                book.getDate().toString(),
                                F.getFlightNum(),
                                currentTimeStamp));
                        break;
                    }
                }
                if (booked==false) {
                    faillist.add(book.getName());
                }
                booked=false;
            }
            if (faillist.size()>0) {
                DropDisplay.append("Customers: ");
                for (String S: faillist) {
                    DropDisplay.append(String.format("%s, ",S));
                }
                DropDisplay.append("fail to rebook due to lack of seats\n\n");
            }
            List<Book> droppedwaitlist=WaitList.findbyFlight(dropflight);
            int k=WaitList.dropbyFlight(dropflight);
            if (k>0) {
                DropDisplay.append("Customer: ");
                for (Book B: droppedwaitlist) {
                    DropDisplay.append(String.format("%s, ",B.getName()));
                }
                DropDisplay.append(String.format("are dropped from waitlist because Flight %s is dropped\n", dropflight));
                
            }
            else {
                DropDisplay.append("No Booking from Wait List is dropped");
            }
        }
        else {
            DropDisplay.append("No Customer need to rebook");
        }
        AddFlightComboBox();
        AddDateComboBox();
        AddNameComboBox();
    }//GEN-LAST:event_DropButtonActionPerformed

