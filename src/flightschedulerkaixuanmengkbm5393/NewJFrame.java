    private void BOOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BOOKActionPerformed
        BookDisplay.setText("");
        Date DateBooked=Date.valueOf(DateBox.getSelectedItem().toString());
        String FlightSelected=FlightBox.getSelectedItem().toString();
        if (BookingList.getEmptySeats(DateBooked, FlightSelected)>0) {
            BookingList.newBook(CustomerName.getText(), DateBooked, FlightSelected);

            Timestamp currentTimeStamp=new Timestamp(Calendar.getInstance().getTime().getTime());
            BookDisplay.append(String.format("Booked Successfully\nBooking information:\n\n"
                    + "Customer Name: %s\n"
                    + "Date: %s\n"
                    + "Flight: %s\n"
                    + "Timestamp: %s\n\n", CustomerName.getText(),DateBooked, FlightSelected,currentTimeStamp));
        }
        else {
            WaitList.addWaitList(CustomerName.getText(), DateBooked, FlightSelected);
            Timestamp currentTimeStamp=new Timestamp(Calendar.getInstance().getTime().getTime());
            BookDisplay.append(String.format("Added to Wait List\nWait List information:\n\n"
                    + "Customer Name: %s\n"
                    + "Date: %s\n"
                    + "Flight: %s\n"
                    + "Timestamp: %s\n\n", CustomerName.getText(),DateBooked, FlightSelected,currentTimeStamp));
        }
        AddNameComboBox();
    }//GEN-LAST:event_BOOKActionPerformed

