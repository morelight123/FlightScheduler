    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed

        Display.setText("");
        String Customer_Name=Cname.getSelectedItem().toString();
        if (SbyName.isSelected()==false) {
            Date DateBooked=Date.valueOf(DateBox1.getSelectedItem().toString());
            String flightBooked=FlightBox1.getSelectedItem().toString();
            List<Book> result=new ArrayList<Book>();
            if (WL_checkBox.isSelected()==false) {
                result=BookingList.findByFlightDate(DateBooked, flightBooked);
            }

            else {
                result=WaitList.findByFlightDate(DateBooked,flightBooked);

            }
            if (result.size()==0) {
                Display.append("No Result");
            }
            else {
            for(Book b: result) {
                Display.append(String.format("Customer: %s\n\n"
                    + "Date: %s\n" + "Flight: %s\n"+"TimeStamp: %s\n\n\n",
                    b.getName(),
                    b.getDate().toString(),
                    b.getFlight(),
                    b.getTimeStamp().toString()));
            }
        }
        }
        else {
            List<Book> result=new ArrayList<Book>();
            result=BookingList.findByName(Customer_Name);
            if (result.isEmpty()) {
                result=WaitList.findByName(Customer_Name);
                if (result.isEmpty()) {
                    Display.setText("No result");
                }
                else {
                    for(Book b: result) {
                        Display.append(String.format("Customer(%s) is on WaitList:\n\n"
                            + "Date: %s\n" + "Flight: %s\n"+"TimeStamp: %s\n\n\n",
                            Customer_Name,
                            b.getDate().toString(),
                            b.getFlight(),
                            b.getTimeStamp().toString()));
                }
            }
        }
        else {
            for(Book b: result) {
                Display.append(String.format("Customer(%s) has trip:\n\n"
                    + "Date: %s\n" + "Flight: %s\n"+"TimeStamp: %s\n\n\n",
                    Customer_Name,
                    b.getDate().toString(),
                    b.getFlight(),
                    b.getTimeStamp().toString()));
        }
        }
        }
    }//GEN-LAST:event_SearchActionPerformed

